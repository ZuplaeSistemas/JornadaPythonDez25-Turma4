# modulo de calculo -  modularização

# responsabilidade única
def soma(n1, n2): # tem dois parâmentros = n1 e n2
    resultado = n1 + n2
    return resultado

def subtracao(n1, n2): # tem dois parâmentros = n1 e n2
    resultado = n1 - n2
    return resultado

def multiplicacao(n1, n2): # tem dois parâmentros = n1 e n2
    resultado = n1 * n2
    return resultado

def divisao(n1, n2): # tem dois parâmentros = n1 e n2
    if n2 != 0:
        resultado = n1 / n2
        return resultado

def exponenciacao(n1, n2): # tem dois parâmentros = n1 e n2
    resultado = n1 ** n2
    return resultado

